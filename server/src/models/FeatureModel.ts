import { Schema, model } from "mongoose";
import { IFeature } from "../types";

const featureSchema = new Schema<IFeature>(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

export default model<IFeature>("Feature", featureSchema);
